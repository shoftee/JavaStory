package javastory.server.handling;

import org.apache.mina.core.session.IoSession;
import org.apache.mina.filter.codec.ProtocolCodecFactory;
import org.apache.mina.filter.codec.ProtocolDecoder;
import org.apache.mina.filter.codec.ProtocolEncoder;

public class GameCodecFactory implements ProtocolCodecFactory {

    private final ProtocolEncoder encoder = new PacketEncoder();
    private final ProtocolDecoder decoder = new PacketDecoder();

    @Override
	public ProtocolEncoder getEncoder(IoSession session) throws Exception {
         return encoder;
    }

    @Override
	public ProtocolDecoder getDecoder(IoSession session) throws Exception {
        return decoder;
    }
}
